        private void loadRecentlyAddedToTable() {
                DefaultTableModel model = (DefaultTableModel) jTable8.getModel();
                model.setRowCount(0);

                // get the recent queue from controller
                Vehicle[] recentQueue = controller.getRecentQueue();
                int front = controller.getQueueFront();
                int rear = controller.getQueueRear();

                // loop through queue from front to rear (FIFO order)
                int sn = 1;
                if (front != -1) {
                        for (int i = front; i <= rear; i++) {
                                Vehicle v = recentQueue[i];
                                if (v != null) {
                                        Object[] row = {
                                                        false, // checkbox
                                                        sn, // serial number
                                                        v.getVehicleId(),
                                                        v.getVehicleType(),
                                                        v.getMake(),
                                                        v.getModel(),
                                                        v.getYear(),
                                                        "$" + v.getPrice(),
                                                        v.getAmount(),
                                                        v.getStatus()
                                        };
                                        model.addRow(row);
                                        sn++;
                                }
                        }
                }
        }

