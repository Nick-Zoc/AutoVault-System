        private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
                // get the search text from the text field
                String searchQuery = jTextField4.getText().trim();

                // if search field is empty, reload all vehicles
                if (searchQuery.isEmpty()) {
                        loadVehicleListToTable();
                        return;
                }

                // call the search method from the controller - returns ALL matching vehicles
                ArrayList<Vehicle> foundVehicles = AutoVaultSystem.performLinearSearch(searchQuery,
                                controller.getVehicleList());

                // check if any vehicles were found
                if (!foundVehicles.isEmpty()) {
                        // clear the table and show all found vehicles
                        DefaultTableModel model = (DefaultTableModel) jTable6.getModel();
                        model.setRowCount(0);

                        // add all found vehicles to the table
                        int sn = 1;
                        for (Vehicle v : foundVehicles) {
                                Object[] row = {
                                                false, // checkbox
                                                sn, // serial number
                                                v.getVehicleId(),
                                                v.getVehicleType(),
                                                v.getMake(),
                                                v.getModel(),
                                                v.getYear(),
                                                "$" + v.getPrice(),
                                                v.getAmount(),
                                                v.getStatus()
                                };
                                model.addRow(row);
                                sn++;
                        }
                } else {
                        // show message that no vehicles were found
                        JOptionPane.showMessageDialog(this, "No vehicles found for: " + searchQuery, "Search Result",
                                        JOptionPane.INFORMATION_MESSAGE);
                        // reload the full table
                        loadVehicleListToTable();
                }
        }

        // method to load vehicle data into jTable6
        private void loadVehicleListToTable() {
                DefaultTableModel model = (DefaultTableModel) jTable6.getModel();

                // clear existing rows
                model.setRowCount(0);

                // loop through each vehicle and add to table
                int sn = 1;
                for (Vehicle v : controller.getVehicleList()) {
                        Object[] row = {
                                        false, // checkbox
                                        sn, // serial number
                                        v.getVehicleId(), // vehicle ID
                                        v.getVehicleType(), // type
                                        v.getMake(), // make
                                        v.getModel(), // model
                                        v.getYear(), // year
                                        "$" + v.getPrice(), // price
                                        v.getAmount(), // amount
                                        v.getStatus() // status
                        };
                        model.addRow(row);
                        sn = sn + 1;
                }
        }

