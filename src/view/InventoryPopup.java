    // method to setup form based on mode (add or edit)
    private void setupFormForMode() {
        if (mode.equals("edit") && vehicleId != null) {
            // find vehicle from controller and fill form
            Vehicle vehicle = controller.findVehicleById(vehicleId);
            if (vehicle != null) {
                jComboBox1.setSelectedItem(vehicle.getVehicleType());
                jTextField4.setText(vehicle.getMake());
                jTextField1.setText(vehicle.getModel());
                jTextField5.setText(String.valueOf(vehicle.getPrice()));
                jTextField2.setText(String.valueOf(vehicle.getAmount()));
                // change title and button for edit mode
                loginButton.setText("üíæ  Save");
                auto1.setText("<html>Edit <span style =\"color: #007FFF\">Inventory</span> </html>");
            }
        } else {
            // add mode - use default title and button text
            auto1.setText("<html>Add to <span style =\"color: #007FFF\">Inventory</span> </html>");
            loginButton.setText("+  Add Item");
        }
    }

