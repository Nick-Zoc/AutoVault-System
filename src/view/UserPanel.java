        // method to load purchase history into jTable8 (FIFO order from LinkedList)
        private void loadPurchaseHistoryToTable() {
                DefaultTableModel model = (DefaultTableModel) jTable8.getModel();
                model.setRowCount(0);

                // get purchase history from controller (LinkedList of Vehicle)
                java.util.LinkedList<Vehicle> history = AutoVaultSystem.getPurchaseHistory();

                // loop through history and add to table (FIFO - first purchased first)
                int sn = 1;
                for (Vehicle v : history) {
                        Object[] row = {
                                        false, // checkbox
                                        sn, // serial number
                                        v.getVehicleId(),
                                        v.getVehicleType(),
                                        v.getMake(),
                                        v.getModel(),
                                        v.getYear(),
                                        "$" + v.getPrice(),
                                        v.getAmount(),
                                        "Purchased" // status
                        };
                        model.addRow(row);
                        sn++;
                }
        }

        // method to load New Arrivals (from recentQueue) into jTable3
        private void loadNewArrivalsToTable() {
                DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
                model.setRowCount(0);

                // get recent queue from controller
                Vehicle[] recentQueue = controller.getRecentQueue();
                int rear = controller.getQueueRear();

                // loop through queue and add to table
                int sn = 1;
                for (int i = 0; i <= rear; i++) {
                        if (recentQueue[i] != null) {
                                Vehicle v = recentQueue[i];
                                Object[] row = {
                                                sn,
                                                v.getVehicleId(),
                                                v.getVehicleType(),
                                                v.getMake(),
                                                v.getModel(),
                                                v.getYear(),
                                                "$" + v.getPrice(),
                                                v.getStatus()
                                };
                                model.addRow(row);
                                sn++;
                        }
                }
        }

